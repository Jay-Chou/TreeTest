package bean;

public class Beans {
	
	private int id;
	private String name;
	private String level_path;
	private int pid;
	private int order;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLevel_path() {
		return level_path;
	}
	public void setLevel_path(String level_path) {
		this.level_path = level_path;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	
}
